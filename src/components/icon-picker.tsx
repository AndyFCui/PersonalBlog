interface IconPickerProps {
  value: string
  onChange: (icon: string) => void
  trigger?: React.ReactNode
}

export function IconPicker({ value, onChange, trigger }: IconPickerProps) {
  const [open, setOpen] = useState(false)

  return (
    <Dialog open={open} onOpenChange={setOpen}>
      <DialogTrigger asChild>
        {trigger || (
          <Button variant="outline" size="icon" className="w-12 h-12 text-2xl">
            {value || '📄'}
          </Button>
        )}
      </DialogTrigger>
      <DialogContent className="sm:max-w-lg">
        <DialogHeader>
          <DialogTitle>选择图标</DialogTitle>
        </DialogHeader>
        <div className="max-h-[400px] overflow-y-auto p-2 space-y-4">
          {emojiCategories.map((category) => (
            <div key={category.name}>
              <p className="text-sm font-medium text-muted-foreground mb-2 px-1">{category.name}</p>
              <div className="grid grid-cols-[repeat(auto-fill,minmax(2.2rem,1fr))] gap-1">
                {category.emojis.map((emoji) => (
                  <button
                    key={emoji}
                    onClick={() => {
                      onChange(emoji)
                      setOpen(false)
                    }}
                    className={`w-9 h-9 text-lg rounded-lg hover:bg-secondary transition-all flex items-center justify-center ${
                      value === emoji ? 'bg-primary/20 ring-2 ring-primary' : ''
                    }`}
                    title={emoji}
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            </div>
          ))}
        </div>
      </DialogContent>
    </Dialog>
  )
}
